    private void grafica() {
        String query = "select * from habitaciones where Disponibilidad = " + "'" + 0 + "'";
        this.conn.Consult(query);
        int x = 0;
        try {//obtengo el numero de renglones con disponibilidad 0, que es ocupado
            this.conn.rs.last();
            x = this.conn.rs.getRow();
            this.conn.rs.first();
        } catch (SQLException ex) {
            System.out.println("Error#1...");
            Logger.getLogger(P7_Consulta_5.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultPieDataset dat = new DefaultPieDataset();
        float por = ((x * 100) / 30);//30 tot de habitaciones
        if (por == 0) {
            dat.setValue(0 + " % Ocupado", 0);
            dat.setValue(100 + " % Disponible", 100);
        } else {
            dat.setValue(por + " % Ocupado", por);
            dat.setValue(100 - por + " % Disponible", 100-por);
        }
        JFreeChart chart = ChartFactory.createPieChart("Total de habitaciones", dat, true, true, false);
        ChartFrame f = new ChartFrame("Numero de habitaciones", chart);

        f.pack();
        f.setVisible(true);
    }
